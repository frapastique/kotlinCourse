/*
    Schreibe hier deine Antwort auf:
        5

        (fixed) Da in der while Schleife keine println(i) ist, wird nichts ausgegeben bis der Wert bis fünf (5) aufaddiert wurde.
        Sobald 5 erreicht wird, wird die Zahl fünf (5) in der Konsole ausgegeben!

